/*********************************************************************************************************
* 函数名称: Get_Temprate
* 函数功能: 获取指定通道的转换值
* 输入参数: void
* 输出参数: void
* 返 回 值: 温度值(扩大了100倍,单位:℃.)
* 创建日期: 2025-11-27
* 注    意:
*********************************************************************************************************/
float Get_Temprate(void)
{
	uint16_t sensor_val;
	float temperature;

	sensor_val = adc_Get_Temp_Value();
	temperature = (float)sensor_val * (3.3 / 4096);
	temperature = ((1.43 - temperature) / 0.0043) + 25;

	return temperature;
}
