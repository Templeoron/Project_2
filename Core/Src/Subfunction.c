void delay_us(uint32_t us)
{
    uint32_t tickstart = SysTick->VAL;  // 获取当前的 SysTick 值
    uint32_t delay_ticks = us * (SystemCoreClock / 1000000);  // 将微秒转换为时钟周期数

    // 等待直到经过的时钟周期数达到目标延时
    while ((tickstart - SysTick->VAL) < delay_ticks)
    {
        // 空循环等待
    }
}
